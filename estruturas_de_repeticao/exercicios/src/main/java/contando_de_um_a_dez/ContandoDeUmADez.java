package contando_de_um_a_dez;

/*
 * Escreva um programa Java que imprime números de 1 à 10
 * utilizando a estrutura de repetição while.
 * O programa deve imprimir cada número em uma
 * linha diferente.
 */
public class ContandoDeUmADez {

    public static void main(String[] args) {

        int iteracao = 1;
        while (iteracao <= 10) {
            System.out.println(iteracao);

            iteracao++;
        }

    }

}
